    status STATUS,
    active BOOLEAN DEFAULT false,
    forwarded_chat_popup_notifications BOOLEAN DEFAULT false,
    forwarded_chat_sound_notifications BOOLEAN DEFAULT true,
    forwarded_chat_email_notifications BOOLEAN DEFAULT false,
    new_chat_popup_notifications BOOLEAN DEFAULT false,
    new_chat_sound_notifications BOOLEAN DEFAULT false,
    use_autocorrect BOOLEAN DEFAULT false,
    new_chat_email_notifications BOOLEAN DEFAULT false,
    created TIMESTAMP WITH TIME ZONE DEFAULT now()
);

