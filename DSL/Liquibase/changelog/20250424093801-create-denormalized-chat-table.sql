WITH 
latest_chats AS (
    SELECT c.id, c.base_id, c.updated
    FROM chat c
    INNER JOIN (
        SELECT base_id, MAX(id) as max_id
        FROM chat
        GROUP BY base_id
    ) latest ON c.base_id = latest.base_id AND c.id = latest.max_id
),
latest_messages AS (
    SELECT m.id, m.chat_base_id AS base_id, m.updated
    FROM message m
    INNER JOIN (
        SELECT chat_base_id, MAX(id) as max_id
        FROM message
        GROUP BY chat_base_id
    ) latest ON m.chat_base_id = latest.chat_base_id AND m.id = latest.max_id
),
latest_chcs AS (
    SELECT chc.id, chc.chat_id AS base_id, chc.created
    FROM chat_history_comments chc
    INNER JOIN (
        SELECT chat_id, MAX(id) as max_id
        FROM chat_history_comments
        GROUP BY chat_id
    ) latest ON chc.chat_id = latest.chat_id AND chc.id = latest.max_id
),
combined_records AS (
    -- Including original ended chats records
    -- Chat history comment records for ended chats
    SELECT
        chc.created AS record_date,
        chc.id AS chat_history_id,
        chc.chat_id,
        chc.comment,
        chc.created AS comment_added_date,
        chc.author_display_name AS comment_author,
        m.min_id,
        m.max_id,
        m.first_message_content,
        m.max_msg_updated AS last_message_timestamp,
        m.max_msg_event AS last_message_event,
        m.max_msg_event_with_content AS last_message_event_with_content,
        m.last_message_content,
        m.last_message_including_empty_content,
        m.customer_messages_count,
        cm.contact_message_content,
        c.customer_support_id,
        m.max_msg_author_id AS author_id,
        c.customer_support_display_name,
        cu.first_name AS customer_support_first_name,
        cu.last_name AS customer_support_last_name,
        c.csa_title,
        c.end_user_id,
        c.end_user_first_name,
        c.end_user_last_name,
        c.end_user_email,
        c.end_user_phone,
        c.end_user_os,
        c.end_user_url,
        c.status,
        c.updated AS chat_updated,
        c.ended,
        c.forwarded_to,
        c.forwarded_to_name,
        c.received_from,
        c.received_from_name,
        c.external_id,
        c.labels,
        m.first_message_timestamp AS first_message_timestamp,
        c.created AS chat_created,
        c.feedback_text,
        c.feedback_rating,
        chc.created,
        fsm.first_support_timestamp,
        CASE
            WHEN fsm.first_support_timestamp IS NOT NULL AND lm.last_timestamp IS NOT NULL
            THEN ABS(EXTRACT(EPOCH FROM (fsm.first_support_timestamp - lm.last_timestamp)))
            ELSE NULL
        END AS duration_seconds,
        -- New columns data
        m.last_message_content_no_rating_forward,
        m.last_message_timestamp_no_rating_forward,
        m.last_non_empty_message_event,
        (
            SELECT array_agg(content ORDER BY id) AS all_message_contents
            FROM message
            WHERE chat_base_id = chc.chat_id
            and content <> ''
        ) AS all_messages
    FROM
        chat_history_comments AS chc
    LEFT JOIN LATERAL (
        SELECT *
        FROM chat AS c
        WHERE c.base_id = chc.chat_id
        ORDER BY c.updated DESC
        LIMIT 1
    ) AS c ON true
    LEFT JOIN LATERAL (
        WITH content_message_bounds AS (
            SELECT
                MAX(id) AS max_content_id,
                MIN(id) AS min_content_id
            FROM message AS m
            WHERE
                m.chat_base_id = chc.chat_id
                AND content <> ''
                AND content <> 'message-read'
        ),
        all_message_bounds AS (
            SELECT
                MAX(id) AS max_id
            FROM message AS m
            WHERE
                m.chat_base_id = chc.chat_id
        ),
        customer_messages_count AS (
            SELECT COUNT(inner_m.id) AS customer_messages_count
            FROM message AS inner_m
            WHERE
                inner_m.chat_base_id = chc.chat_id
                AND inner_m.author_role = 'end-user'
                AND (inner_m.event = '' OR inner_m.event IS NULL)
        ),
        -- Add CTEs for new columns
        content_not_rating_forward_message AS (
            SELECT 
                MAX(id) AS max_content_not_rating_forward_id
            FROM message AS m
            WHERE
                m.chat_base_id = chc.chat_id
                AND m.content <> ''
                AND m.content <> 'message-read'
                AND m.event <> 'rating'
                AND m.event <> 'requested-chat-forward'
        ),
        not_rating_forward_message AS (
            SELECT 
                MAX(id) AS max_not_rating_forward_id
            FROM message AS m
            WHERE
                m.chat_base_id = chc.chat_id
                AND m.event <> 'rating'
                AND m.event <> 'requested-chat-forward'
        ),
        non_empty_event_message AS (
            SELECT 
                MAX(id) AS max_non_empty_event_id
            FROM message AS m
            WHERE
                m.chat_base_id = chc.chat_id
                AND m.event <> ''
                AND m.event IS NOT NULL
        )
        SELECT
            cmb.min_content_id AS min_id,
            cmb.max_content_id AS max_id,
            min_msg.created AS first_message_timestamp,
            min_msg.content AS first_message_content,
            max_content_msg.content AS last_message_content,
            max_msg.content AS last_message_including_empty_content,
            max_msg.updated AS max_msg_updated,
            max_msg.event AS max_msg_event,
            max_content_msg.event AS max_msg_event_with_content,
            max_msg.author_id AS max_msg_author_id,
            cmc.customer_messages_count,
            -- Get data for new columns
            content_not_rating_msg.content AS last_message_content_no_rating_forward,
            not_rating_msg.updated AS last_message_timestamp_no_rating_forward,
            non_empty_msg.event AS last_non_empty_message_event
        FROM content_message_bounds AS cmb
        CROSS JOIN all_message_bounds AS amb
        CROSS JOIN customer_messages_count AS cmc
        LEFT JOIN message AS min_msg ON min_msg.id = cmb.min_content_id
        LEFT JOIN message AS max_content_msg ON max_content_msg.id = cmb.max_content_id
        LEFT JOIN message AS max_msg ON max_msg.id = amb.max_id
        -- Join tables for new columns
        LEFT JOIN content_not_rating_forward_message AS crf ON TRUE
        LEFT JOIN message AS content_not_rating_msg ON content_not_rating_msg.id = crf.max_content_not_rating_forward_id
        LEFT JOIN not_rating_forward_message AS nrf ON TRUE
        LEFT JOIN message AS not_rating_msg ON not_rating_msg.id = nrf.max_not_rating_forward_id
        LEFT JOIN non_empty_event_message AS nem ON TRUE
        LEFT JOIN message AS non_empty_msg ON non_empty_msg.id = nem.max_non_empty_event_id
    ) AS m ON TRUE
    LEFT JOIN LATERAL (
        SELECT
            m.content AS contact_message_content
        FROM message AS m
        WHERE m.chat_base_id = chc.chat_id
        AND m.event = 'contact-information-fulfilled'
        ORDER BY m.id DESC
        LIMIT 1
    ) AS cm ON TRUE
    LEFT JOIN LATERAL (
        SELECT
            first_name,
            last_name
        FROM "user" AS u
        WHERE u.id_code = c.customer_support_id
        ORDER BY u.created DESC
        LIMIT 1
    ) AS cu ON TRUE
    LEFT JOIN LATERAL (
        SELECT
            chat_base_id,
            MIN(author_timestamp) AS first_support_timestamp
        FROM message
        WHERE
            chat_base_id = chc.chat_id
            AND author_role = 'backoffice-user'
        GROUP BY chat_base_id
    ) AS fsm ON TRUE    
    LEFT JOIN LATERAL (
        SELECT
            chat_base_id,
            MAX(author_timestamp) AS last_timestamp
        FROM message
        WHERE
            chat_base_id = chc.chat_id
        GROUP BY chat_base_id
    ) AS lm ON TRUE
    WHERE c.base_id IS NOT NULL
    AND NOT EXISTS (
        SELECT 1
        FROM chat_history_comments AS chc_inner
        WHERE chc_inner.chat_id = chc.chat_id
        AND chc_inner.created = chc.created
        AND chc_inner.id < chc.id
    )
    AND chc.id IN (select latest_chcs.id from latest_chcs where base_id = chc.chat_id)
    AND (
        chc.created > COALESCE((SELECT MAX(updated) FROM latest_chats WHERE base_id = chc.chat_id), '1900-01-01'::timestamp)
        AND chc.created > COALESCE((SELECT MAX(updated) FROM latest_messages WHERE base_id = chc.chat_id), '1900-01-01'::timestamp)
    )

    UNION ALL

    -- Chat records without matching history comments (for ended chats)
    SELECT
        c.updated AS record_date,
        chc_latest.id AS chat_history_id,
        c.base_id AS chat_id,
