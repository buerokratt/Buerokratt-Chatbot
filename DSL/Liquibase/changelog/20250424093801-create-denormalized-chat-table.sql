            FROM message
            WHERE chat_base_id = c.base_id
            and content <> ''
        ) AS all_messages
    FROM
        chat AS c
    LEFT JOIN LATERAL (
        SELECT *
        FROM chat_history_comments AS chc
        WHERE chc.chat_id = c.base_id
        ORDER BY chc.created DESC
        LIMIT 1
    ) AS chc_latest ON true
    LEFT JOIN LATERAL (
        WITH content_message_bounds AS (
            SELECT
                MAX(id) AS max_content_id,
                MIN(id) AS min_content_id
            FROM message AS m
            WHERE
                m.chat_base_id = c.base_id
                AND content <> ''
                AND content <> 'message-read'
        ),
        all_message_bounds AS (
            SELECT
                MAX(id) AS max_id
            FROM message AS m
            WHERE
                m.chat_base_id = c.base_id
        ),
        customer_messages_count AS (
