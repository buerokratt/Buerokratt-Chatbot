        END AS last_message_event_with_content,
        CASE
            WHEN m.content <> '' AND m.content <> 'message-read' THEN m.content
            ELSE (SELECT content
                FROM message
                WHERE chat_base_id = m.chat_base_id
                AND content <> ''
                AND content <> 'message-read'
                ORDER BY id DESC
                LIMIT 1)
        END AS last_message_content,
        m.content AS last_message_including_empty_content,
        cmc.customer_messages_count,
        cm.contact_message_content,
        c.customer_support_id,
        m.author_id,
        c.customer_support_display_name,
        cu.first_name AS customer_support_first_name,
        cu.last_name AS customer_support_last_name,
        c.csa_title,
        c.end_user_id,
        c.end_user_first_name,
        c.end_user_last_name,
        c.end_user_email,
        c.end_user_phone,
        c.end_user_os,
        c.end_user_url,
        c.status,
        c.updated AS chat_updated,
        c.ended,
        c.forwarded_to,
        c.forwarded_to_name,
        c.received_from,
        c.received_from_name,
        c.external_id,
        c.labels,
        m_msg.created AS first_message_timestamp,
        c.created AS chat_created,
        c.feedback_text,
        c.feedback_rating,
        m.created,
        fsm.first_support_timestamp,
        CASE
            WHEN fsm.first_support_timestamp IS NOT NULL AND lm.last_timestamp IS NOT NULL
            THEN ABS(EXTRACT(EPOCH FROM (fsm.first_support_timestamp - lm.last_timestamp)))
            ELSE NULL
        END AS duration_seconds,
        -- New columns data
        (SELECT content
         FROM message
         WHERE chat_base_id = m.chat_base_id
         AND content <> ''
         AND content <> 'message-read'
         AND event <> 'rating'
         AND event <> 'requested-chat-forward'
         ORDER BY id DESC
         LIMIT 1) AS last_message_content_no_rating_forward,
        (SELECT updated
         FROM message
         WHERE chat_base_id = m.chat_base_id
         AND event <> 'rating'
         AND event <> 'requested-chat-forward'
         ORDER BY id DESC
         LIMIT 1) AS last_message_timestamp_no_rating_forward,
        (SELECT event
         FROM message
         WHERE chat_base_id = m.chat_base_id
         AND event <> ''
         AND event IS NOT NULL
         ORDER BY id DESC
         LIMIT 1) AS last_non_empty_message_event,
         (
            SELECT array_agg(content ORDER BY id) AS all_message_contents
            FROM message
            WHERE chat_base_id = m.chat_base_id
            and content <> ''
        ) AS all_messages
    FROM
        message AS m
    LEFT JOIN LATERAL (
        SELECT *
        FROM chat_history_comments AS chc
        WHERE chc.chat_id = m.chat_base_id
        ORDER BY chc.created DESC
        LIMIT 1
    ) AS chc_latest ON true
    LEFT JOIN LATERAL (
        SELECT *
        FROM chat AS c
        WHERE c.base_id = m.chat_base_id
        ORDER BY c.updated DESC
        LIMIT 1
    ) AS c ON true
    LEFT JOIN LATERAL (
        WITH message_bounds AS (
            SELECT
                MIN(id) AS min_id
            FROM message AS inner_m
            WHERE
                inner_m.chat_base_id = m.chat_base_id
                AND inner_m.content <> ''
                AND inner_m.content <> 'message-read'
        )
        SELECT
            mb.min_id,
            m.id AS max_id
        FROM message_bounds AS mb
    ) AS m_bounds ON TRUE
    LEFT JOIN message AS m_msg ON m_msg.id = m_bounds.min_id
    LEFT JOIN LATERAL (
        SELECT COUNT(inner_m.id) AS customer_messages_count
        FROM message AS inner_m
        WHERE
            inner_m.chat_base_id = m.chat_base_id
            AND inner_m.author_role = 'end-user'
            AND (inner_m.event = '' OR inner_m.event IS NULL)
    ) AS cmc ON TRUE
    LEFT JOIN LATERAL (
        SELECT
            m_cm.content AS contact_message_content
        FROM message AS m_cm
        WHERE m_cm.chat_base_id = m.chat_base_id
        AND m_cm.event = 'contact-information-fulfilled'
        ORDER BY m_cm.id DESC
        LIMIT 1
    ) AS cm ON TRUE
    LEFT JOIN LATERAL (
        SELECT
            first_name,
            last_name
        FROM "user" AS u
        WHERE u.id_code = c.customer_support_id
        ORDER BY u.created DESC
        LIMIT 1
    ) AS cu ON TRUE
    LEFT JOIN LATERAL (
        SELECT
            chat_base_id,
            MIN(author_timestamp) AS first_support_timestamp
        FROM message
        WHERE
            chat_base_id = m.chat_base_id
            AND author_role = 'backoffice-user'
        GROUP BY chat_base_id
    ) AS fsm ON TRUE
    LEFT JOIN LATERAL (
        SELECT
            chat_base_id,
            MAX(author_timestamp) AS last_timestamp
        FROM message
        WHERE
            chat_base_id = m.chat_base_id
        GROUP BY chat_base_id
    ) AS lm ON TRUE
    WHERE c.base_id IS NOT NULL
    AND NOT EXISTS (
        SELECT 1
        FROM chat_history_comments AS chc
        WHERE chc.chat_id = m.chat_base_id
        AND chc.created = m.updated
    )
    AND NOT EXISTS (
        SELECT 1
        FROM chat AS cx
        WHERE cx.base_id = m.chat_base_id
        AND cx.updated = m.updated
    )
    AND m.id IN (select latest_messages.id from latest_messages where latest_messages.base_id = m.chat_base_id)
    AND (
        m.updated > COALESCE((SELECT MAX(updated) FROM latest_chats WHERE base_id = m.chat_base_id), '1900-01-01'::timestamp)
    )
)
SELECT
    chat_id,
    customer_support_id,
    customer_support_display_name,
    customer_support_first_name,
    customer_support_last_name,
    end_user_id,
    end_user_first_name,
    end_user_last_name,
    end_user_email,
    end_user_phone,
    end_user_os,
    end_user_url,
    status,
    first_message_timestamp,
    author_id,
    chat_created AS created,
    chat_updated AS updated,
    record_date AS denormalized_record_created,
    ended,
    forwarded_to,
    forwarded_to_name,
    received_from,
    received_from_name,
    external_id,
    labels,
    comment,
    comment_added_date,
    comment_author,
    first_message_content AS first_message,
    last_message_content AS last_message,
    last_message_including_empty_content,
    contact_message_content AS contacts_message,
    last_message_timestamp,
    feedback_text,
    feedback_rating,
    csa_title,
    CASE
        WHEN last_message_event IS NULL THEN NULL
        ELSE LOWER(last_message_event)
    END AS last_message_event,
    CASE
        WHEN last_message_event_with_content IS NULL THEN NULL
        ELSE LOWER(last_message_event_with_content)
    END AS last_message_event_with_content,
    duration_seconds AS chat_duration_in_seconds,
    first_support_timestamp,
    customer_messages_count,
    -- New columns
    last_message_content_no_rating_forward AS last_message_with_content_and_not_rating_or_forward,
    last_message_timestamp_no_rating_forward AS last_message_with_not_rating_or_forward_events_timestamp,
    CASE
        WHEN last_non_empty_message_event IS NULL THEN NULL
        ELSE LOWER(last_non_empty_message_event)
    END AS last_non_empty_message_event,
    all_messages
FROM combined_records
WHERE chat_id IN (SELECT base_id FROM chat)
ORDER BY denormalized_record_created;
