-- Display the results from both queries side by side
SELECT 'Test Results Comparison:' AS section;
SELECT
    orig.test_case,
    orig.user_id_code AS original_result,
    denorm.user_id_code AS denormalized_result,
    CASE
        WHEN
            (orig.user_id_code IS NULL AND denorm.user_id_code IS NULL)
            OR (orig.user_id_code = denorm.user_id_code) THEN 'MATCH'
        ELSE 'MISMATCH'
    END AS comparison
FROM original_query_results AS orig
    FULL OUTER JOIN denorm_query_results AS denorm ON orig.test_case = denorm.test_case
ORDER BY orig.test_case;

-- Calculate overall test result
SELECT 'Overall Test Result:' AS section;
SELECT
    CASE
        WHEN EXISTS (
            SELECT 1
            FROM original_query_results AS orig
                FULL OUTER JOIN
                    denorm_query_results AS denorm
                    ON orig.test_case = denorm.test_case
            WHERE
                (orig.user_id_code IS NULL AND denorm.user_id_code IS NOT NULL)
                OR (orig.user_id_code IS NOT NULL AND denorm.user_id_code IS NULL)
                OR (orig.user_id_code <> denorm.user_id_code)
        ) THEN 'FAIL: Queries return different results'
        ELSE 'PASS: Queries return identical results'
    END AS test_result;

